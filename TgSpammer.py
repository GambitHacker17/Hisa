    async def make_request(self, phone: str, proxy: Optional[dict], service: str, 
                         bot_id: Optional[str], origin: str) -> bool:
        """Make request to specific service"""
        try:
            headers = {
                'User-Agent': random.choice(self.user_agents),
                'Accept': '*/*',
                'Accept-Language': 'ru-RU,ru;q=0.9',
                'Content-Type': 'application/x-www-form-urlencoded',
                'X-Requested-With': 'XMLHttpRequest'
            }

            if service in ["telegram_ads", "my_telegram_org"]:
                url = f"{origin}/auth/"
                if service == "my_telegram_org":
                    url += "send_password"
                data = {'phone': phone}
            else:
                url = "https://oauth.telegram.org/auth/request"
                data = {
                    'phone': phone,
                    'bot_id': bot_id,
                    'origin': origin,
                    'request_access': 'write'
                }

            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                response = requests.post(
                    url,
                    headers=headers,
                    data=data,
                    proxies=proxy,
                    timeout=10
                )

            return response.status_code == 200

        except Exception:
            return False

